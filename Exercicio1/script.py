numero1 = input("Informe o primeiro número: ")
numero2 = input("Informe o segundo número: ")
numero1 = int(numero1)
numero2 = int(numero2)
soma = numero1 + numero2
print(f"A soma de {numero1} e {numero2} é: {soma}")
subtração = numero1 - numero2
print(f"A subtração de {numero1} e {numero2} é: {subtração}")
multiplicação = numero1 * numero2
print(f"A multiplicação de {numero1} e {numero2} é: {multiplicação}")
divisão = numero1 / numero2
print(f"A divisão de {numero1} e {numero2} é: {divisão:.2f}")
resto = numero1 % numero2
print(f"O resto da divisão de {numero1} por {numero2} é: {resto}")
expoente = numero1 ** numero2
print(f"{numero1} elevado a {numero2} é: {expoente}")
